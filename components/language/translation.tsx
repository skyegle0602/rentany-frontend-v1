// Supported language codes
type LanguageCode = 'en' | 'fr' | 'es' | 'de';

// Get browser language and return supported language code
export const detectLanguage = (): LanguageCode => {
  const browserLang = navigator.language || (navigator as any).userLanguage;
  const langCode = browserLang.split('-')[0].toLowerCase() as LanguageCode;
  
  // Return the language if supported, otherwise default to English
  return translations[langCode] ? langCode : 'en';
};

// Get translation by key path (e.g., "nav.browseAll")
export const getTranslation = (lang: string, keyPath: string) => {
  const keys = keyPath.split('.');
  const langCode = lang as LanguageCode;
  let result: any = translations[langCode] || translations.en;
  
  for (const key of keys) {
    if (result && result[key] !== undefined) {
      result = result[key];
    } else {
      // Fallback to English if key not found
      result = translations.en;
      for (const k of keys) {
        if (result && result[k] !== undefined) {
          result = result[k];
        } else {
          return keyPath; // Return key if not found
        }
      }
      break;
    }
  }
  
  return result;
};

